package DecisionStatements.IFElse;

public class Ifelse_01 {

    public static void main(String[] args) {
        String statement1 = "I want to learn Java";
        String statement2 = "I want to learn Python";

        for (int i=1 ; i<=2 ;i++)
        {
            if (i==1)
            {
                System.out.println(statement1);
            }
            else
            {
                System.out.println(statement2);
            }
        }
    }
}
